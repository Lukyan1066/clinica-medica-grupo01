     private void validarCamposObrigatorios() throws CampoObrigatorioException {
        List<String> campos = new ArrayList<>();

        if (nomeField.getText().isBlank()) {
            campos.add("Nome");
        }

        if (!campos.isEmpty()) {
            throw new CampoObrigatorioException(campos);
        }
    }

    private void loadDataInForm(long idUsuario) {
        this.usuarioDataForm = this.usuarioDao.buscarPorId(idUsuario);

        if (usuarioDataForm == null) {
            JOptionPane.showMessageDialog(this, "Registro inválido para atualização!", "Status da operação", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }

        nomeField.setText(usuarioDataForm.getUsuario());
        senhaField.setText(usuarioDataForm.getSenha());
        cargoField.setText(usuarioDataForm.getCargo());
    }
    

