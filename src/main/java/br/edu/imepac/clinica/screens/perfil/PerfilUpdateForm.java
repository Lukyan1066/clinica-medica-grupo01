    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String senhaString = new String(senhaField.getPassword());
        try {

            validarCamposObrigatorios();

            Usuario usuario = new Usuario(this.usuarioDataForm.getId(), nomeField.getText(), senhaString, cargoField.getText());

            boolean status = this.usuarioDao.atualizar(usuario);

            if (status == true) {
                JOptionPane.showMessageDialog(this, "Usuario atualizado com sucesso!", "Status da operação", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar o usuario - contate o administrador!", "Status da operação", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (CampoObrigatorioException ex) {
            System.getLogger(PerfilUpdateForm.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            JOptionPane.showMessageDialog(this, "Campos obrigatórios devem ser preenchidos!", "Status da operação", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

