    private void loadDataInTableFromDataBase() {
        UsuarioDao usuarioDao = new UsuarioDao();
        List<Usuario> usuarios = usuarioDao.listarTodos();

        // obtem o objeto que representa os dados da tabela
        DefaultTableModel modelo = (DefaultTableModel) tablePerfil.getModel();
        
        // Limpa os dados atuais na tabela de dados
        modelo.setRowCount(0);

        // Adiciona os dados da lista
        for (Usuario e : usuarios) {
            // Cria o objeto que representa a linha da tabela a ser inserida
            Object[] linha = new Object[]{e.getId(), e.getUsuario(), e.getCargo(), e.getStatus()};
            // adiciona a linha na tabela de dados
            modelo.addRow(linha);
        }

        // Atualiza a tabela (opcional)
        tablePerfil.repaint();
    }
    
