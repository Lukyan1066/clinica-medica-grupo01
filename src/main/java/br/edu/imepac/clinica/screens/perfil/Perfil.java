    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linhaSelecionada = tablePerfil.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um perfil para edição.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Obtém o ID da primeira coluna (índice 0)
        long id = (long) tablePerfil.getValueAt(linhaSelecionada, 0);

        // Chamando o formulário para atualização da especialidade
        PerfilUpdateForm perfilUpdateForm = new PerfilUpdateForm(id);
        perfilUpdateForm.pack();
        perfilUpdateForm.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
  int linhaSelecionada = tablePerfil.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um perfil para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Obtém o ID da primeira coluna (índice 0)
        long id = (long) tablePerfil.getValueAt(linhaSelecionada, 0);
        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o perfil de ID " + id + "?", "Confirmação", JOptionPane.YES_NO_OPTION);

        if (opcao == JOptionPane.YES_OPTION) {
            UsuarioDao usuarioDao = new UsuarioDao();
            boolean sucesso = usuarioDao.excluir(id);

            if (sucesso) {
                JOptionPane.showMessageDialog(null, "Perfil excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                // Atualizar a tabela de exibição com os dados
                loadDataInTableFromDataBase();
            } else {
                JOptionPane.showMessageDialog(null, "Falha ao excluir a especialidade.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int linhaSelecionada = tablePerfil.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um perfil para alterar o status.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Obtém o ID da primeira coluna (índice 0)
        long id = (long) tablePerfil.getValueAt(linhaSelecionada, 0);
        Object status =  tablePerfil.getValueAt(linhaSelecionada, 3);
        int opcao = JOptionPane.showConfirmDialog(null, "Deseja realmente alterar o status do perfil de ID " + id + "?", "Confirmação", JOptionPane.YES_NO_OPTION);

        if (opcao == JOptionPane.YES_OPTION) {
            UsuarioDao usuarioDao = new UsuarioDao();
            boolean sucesso = usuarioDao.alterarStatus(id, status);

            if (sucesso) {
                JOptionPane.showMessageDialog(null, "Status alterado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                // Atualizar a tabela de exibição com os dados
                loadDataInTableFromDataBase();
            } else {
                JOptionPane.showMessageDialog(null, "Falha ao alterar o status.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

